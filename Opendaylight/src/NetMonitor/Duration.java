package NetMonitor;

public class Duration {
	private long nanosecond;
	private long second;
	public long getNanosecond() {
		return nanosecond;
	}
	public void setNanosecond(long nanosecond) {
		this.nanosecond = nanosecond;
	}
	public long getSecond() {
		return second;
	}
	public void setSecond(long second) {
		this.second = second;
	}

}
